package com.game.fight.utils;

public class Velocity {
    private double x;
    private double y;

    public Velocity(double x, double y) {
        this.x = x;
        this.y = y;
    }

	public double getX() {
		return x;
	}

	public double getY() {
		return y;
	}

    // Getters and setters for x and y
}
